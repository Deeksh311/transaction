
public class ArithmeticOperations {
	
  int FirstNumber;
  int SecondNumber;
  
  int add()
  {
	  return FirstNumber+SecondNumber;
  }
  int subtract()
  {
	  return FirstNumber-SecondNumber;
  }
  int multiply()
  {
	  return FirstNumber*SecondNumber;
  }
  float division()
  {
	  return FirstNumber/SecondNumber;
  }
  int remainder()
  {
	  return FirstNumber%SecondNumber;
  }
}
